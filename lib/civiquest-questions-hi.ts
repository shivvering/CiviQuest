/**
 * Hindi display translations for every question, keyed by question id.
 * IMPORTANT: answers are always SAVED as the English option text (canonical),
 * so the research data and teacher dashboard stay consistent — these strings
 * are for display only. Option order matches the English array exactly.
 */
type QuestionHi = {
  question: string;
  options: string[];
  feedback: string;
};

export function localizeQuestion(
  q: CiviQuestion,
  lang: Lang,
): { question: string; options: string[]; feedback: string } {
  if (lang === "hi") {
    const hi = QUESTIONS_HI[q.id];
    if (hi) {
      return hi;
    }
  }
  return { question: q.question, options: q.options, feedback: q.feedback };
}

